MODEL SMALL
.STACK 100H
.DATA
NUM DB 06H,08H,14H,05H,03H
LARGEE DB 0H
.CODE
	MOV AX, @DATA
	MOV DS, AX
	MOV CX, 5
	MOV AL,0H
	LEA SI,NUM

CHECK:  MOV BL,[SI]
	CMP AL,BL
	JNC NOCARRY
	INC SI
	MOV AL,BL
	DEC CX
	JNZ CHECK
	MOV LARGEE,AH
	MOV AH,4CH
	INT 21H
NOCARRY:INC SI	
	DEC CX
	JNZ CHECK
	MOV LARGEE,AH
MOV DX,LARGEE
   ADD Dl,49
   MOV AH,2
   INT 21H
	MOV AH,4CH
	INT 21H
END